package cn.gtmap.msurveyplat.common.dto;

import java.io.Serializable;
import java.util.List;

/**
 * @author <a href="mailto:liujie@gtmap.cn">liujie</a>
 * @version 1.0, 2020/4/15
 * @description 节点信息
 */
public class UserTaskDto implements Serializable {
    protected String id;
    protected String name;
    protected String assignee;
    protected String owner;
    protected String priority;
    protected String formKey;
    protected String dueDate;
    protected String category;
    protected List<String> candidateUsers;
    protected List<String> candidateGroups;
    protected List<FormPropertyDto> formProperties;

    public String getId()
    {
        return this.id;
    }

    public void setId(String id)
    {
        this.id = id;
    }

    public String getName()
    {
        return this.name;
    }

    public void setName(String name)
    {
        this.name = name;
    }

    public String getAssignee()
    {
        return this.assignee;
    }

    public void setAssignee(String assignee)
    {
        this.assignee = assignee;
    }

    public String getOwner()
    {
        return this.owner;
    }

    public void setOwner(String owner)
    {
        this.owner = owner;
    }

    public String getPriority()
    {
        return this.priority;
    }

    public void setPriority(String priority)
    {
        this.priority = priority;
    }

    public String getFormKey()
    {
        return this.formKey;
    }

    public void setFormKey(String formKey)
    {
        this.formKey = formKey;
    }

    public String getDueDate()
    {
        return this.dueDate;
    }

    public void setDueDate(String dueDate)
    {
        this.dueDate = dueDate;
    }

    public String getCategory()
    {
        return this.category;
    }

    public void setCategory(String category)
    {
        this.category = category;
    }

    public List<String> getCandidateUsers()
    {
        return this.candidateUsers;
    }

    public void setCandidateUsers(List<String> candidateUsers)
    {
        this.candidateUsers = candidateUsers;
    }

    public List<String> getCandidateGroups()
    {
        return this.candidateGroups;
    }

    public void setCandidateGroups(List<String> candidateGroups)
    {
        this.candidateGroups = candidateGroups;
    }

    public List<FormPropertyDto> getFormProperties()
    {
        return this.formProperties;
    }

    public void setFormProperties(List<FormPropertyDto> formProperties)
    {
        this.formProperties = formProperties;
    }
}
