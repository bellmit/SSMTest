package cn.gtmap.msurveyplat.common.domain.dchyxmgl;

import javax.persistence.Id;
import javax.persistence.Table;
import java.util.Date;

/**
 * @author <a href="mailto:lijun1@gtmap.cn">lijun1</a>
 * @version 1.0, 2020/11/27 10:55
 * @description 多测合一模板配置
 */
@Table(name = "DCHY_XMGL_MB")
public class DchyXmglMb {
    @Id
    private String mbid; //模板配置id
    private String mbmc; //模板名称
    private String mbqyzt; //模板启用状态(字典项A.10)
    private String wjzxid; //模板文件中心id
    private String mblx; //模板类型(字典项A.6)
    private Date scsj; //上传时间
    private String scr; //上传人
    private String bz; //备注

    public String getBz() { return bz; }

    public void setBz(String bz) { this.bz = bz; }

    public String getMbid() {
        return mbid;
    }

    public void setMbid(String mbid) {
        this.mbid = mbid;
    }

    public String getMbmc() {
        return mbmc;
    }

    public void setMbmc(String mbmc) {
        this.mbmc = mbmc;
    }

    public String getMbqyzt() {
        return mbqyzt;
    }

    public void setMbqyzt(String mbqyzt) {
        this.mbqyzt = mbqyzt;
    }

    public String getWjzxid() {
        return wjzxid;
    }

    public void setWjzxid(String wjzxid) {
        this.wjzxid = wjzxid;
    }

    public String getMblx() {
        return mblx;
    }

    public void setMblx(String mblx) {
        this.mblx = mblx;
    }

    public Date getScsj() {
        return scsj;
    }

    public void setScsj(Date scsj) {
        this.scsj = scsj;
    }

    public String getScr() {
        return scr;
    }

    public void setScr(String scr) {
        this.scr = scr;
    }
}
