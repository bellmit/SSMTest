package cn.gtmap.onemap.platform.entity.video;

import java.io.Serializable;

/**
 * Created by yxf on 2016/5/4.
 */
public final class Ptz implements Serializable {

    private static final long serialVersionUID = 645828072315088796L;

    private double p;

    private double t;

    private int z;

    private double distance;

    public double getP() {
        return p;
    }

    public void setP(double p) {
        this.p = p;
    }

    public double getT() {
        return t;
    }

    public void setT(double t) {
        this.t = t;
    }

    public int getZ() {
        return z;
    }

    public void setZ(int z) {
        this.z = z;
    }

    public double getDistance() {
        return distance;
    }

    public void setDistance(double distance) {
        this.distance = distance;
    }

    public Ptz() {
    }

    public Ptz(double p, double t, int z) {
        this.p = p;
        this.t = t;
        this.z = z;
    }

    @Override
    public String toString() {
        return "PTZ:{" +
                "p=" + p +
                ", t=" + t +
                ", z=" + z +
                ", distance=" + distance +
                "}";
    }
}
